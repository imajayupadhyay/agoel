        <p>A sharp line between schooling that produces certificates and education that produces thinking &mdash; <em>scientific temper in the real sense</em>: the ability to ask, to demand evidence, and to arrive at one&rsquo;s own conclusion rather than the one handed down. An education that lets a young person keep their own voice.</p>
      </div>
      <div class="pr reveal" data-d="1">
        <span class="no">02</span>
        <h3>Systems over slogans</h3>
        <p>Real change arrives when <em>positions change</em> &mdash; when the overlooked gain leverage they didn&rsquo;t have, when an institution behaves differently rather than announcing that it will. Help a person today; change the system they negotiate against tomorrow.</p>
      </div>
      <div class="pr reveal" data-d="2">
        <span class="no">03</span>
        <h3>Dignity for the overlooked</h3>
        <p>A consistent sympathy for those a hierarchical society is trained to look past. The giving is an argument against inherited privilege and in favour of <em>earned dignity</em> &mdash; a person&rsquo;s labour, mind and potential have value independent of name or lineage.</p>
      </div>
    </div>
  </div>
